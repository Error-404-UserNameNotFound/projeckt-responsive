		<h3 class="head">Ziel und Zweck</h3>
		<p>Die Erfassung von Anwesenheitslisten zu Übungen und Gruppenstunden und ihr nachträglicher 
		Übertrag in den Computer benötigt viel Zeit und ist fehleranfällig. 
		Ziel war es, ein einfaches elektronisches System zu schaffen, das die Anwesenheit der Mitglieder 
		bei Übungs und Gruppenstunden unmittelbar elektronisch erfasst und in ein Format bringt, 
		das sich leicht in PC Programme importieren lässt. 
		Als Hardware wird ein Arduino UNO verwendet. 
		Diese Beschreibung ist für Nutzer gestaltet, die schon etwas Erfahrung mit der ARDUINO 
		Umgebung gesammelt haben. Absolute ARDUINO Neulinge werden sich schwer tun.</p>
		<p>Stückliste und Aufbau: 
		Wer die verwendeten Komponenten erst beschaffen muss, sollte Preise und Angebote umfassend vergleichen. 
		Die Unterschiede zwischen einzelnen Anbietern betragen mehrere hundert Prozent.
		Alle Preise sollen nur als Anhaltspunkt gelten. 
		Zu den angegeben Preisen kann man die Komponenten im Januar 2015 beziehen.</p>
	<h4>Aufbau:</h4>
	<ul>
		<li>ARDUINO MEGA 2560 oder kompatibel </li>
		<li>LCD Display 20x4 Zeichen mit I2C Anschluss(gibt es in verschiedenen Farben: blau, grün und orange)</li>
		<li>Aktiver Buzzer (für Signalton)</li>
		<li>Data Logger </li>
		<li>Shield mit SD</li>
		<li>Karten Steckplatz und RTC (Uhr)</li>
		<li>Alternativ als einzelne Komponenten</li>
		<li>RFID RC522 Leser für RFID tags.</li>
		<li>Zwei Taster</li>
		<li>Zwei 10kO Widerstände (als Pull Down Widerstände)</li>
		<li>Gehäuse aus Holz</li>

		<li>USB Kabel zu Programmierung des ARDUINO</li>
		<li>Diverse Kabel mit Steckern und Muffen</li>
		<li>Doppelseitiges Klebeband</li>
	</ul>
		<h4>Außerdem:</h4>
	<ul>
		<li>SD Karte</li>
		<li>RFID Tags oder Karten (13.56MHz)</li>
		<li>Netzteil für ARDUINO vom Elektroschrott</li>
	</ul>
