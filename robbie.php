				<h3 class="head">Einführung in die Wunderbare Welt der Arduino Robotik:</h3>
					<p>Arduino ist eine aus Soft- und Hardware bestehende Physical-Computing-Plattform. Beide Komponenten sind im Sinne von Open Source quelloffen. Die Hardware besteht aus einem einfachen E/A-Board mit einem Mikrocontroller und analogen und digitalen Ein- und Ausgängen. Die Entwicklungsumgebung basiert auf Processing und soll auch technisch weniger Versierten den Zugang zur Programmierung und zu Mikrocontrollern erleichtern. Die Programmierung selbst erfolgt in C bzw. C++, wobei technische Details wie Header-Dateien vor den Anwendern weitgehend verborgen werden und umfangreiche Bibliotheken und Beispiele die Programmierung vereinfachen. Arduino kann verwendet werden, um eigenständige interaktive Objekte zu steuern oder um mit Softwareanwendungen auf Computern zu interagieren (z. B. Adobe Flash, Processing, Max/MSP, Pure Data, SuperCollider, diversen Skriptsprachen, Terminal, vvvv etc.). Arduino wird beispielsweise auch an Kunsthochschulen genutzt, um interaktive Installationen aufzubauen.</p>

					<p>Robby ist ein kleiner, von einem Arduino gesteuerter fahrbarer Roboter.
					Robby wird über eine USB-Schnittstelle programmiert.Die Programmiersprache ist „C“. 
					Die Programmieroberfläche ist recht einfach gestaltet und für Kinder ab ca. 12 Jahren geeignet.</p>
					<p>Robby besteht aus folgenden Komponenten:</p>
				<ul>
					<li>Hardwaresatz mit Getriebemotoren, Halterungen, Rädern, Schrauben usw. </li>
					<li>Arduino Bausatz  </li>
					<li>Bausatz Multifunktionsplatine</li>
					<li>Lichtleistensatz, Tastenleiste</li>
					<li>Bluetooth-Modul </li>
					<li>RF-Modul</li>
					<li>Ping-Modul</li>
					<li>Line-Follower-Modul</li>
					<li>Kompass-Modul</li>
					<li>Lage-Modul</li>
					<li>Infrarot-Fernsteuerung</li>
				</ul>

					<p>Robby gibt es nur als einzelne Komponenten. Das Projekt ist modular 
					aufgebaut und kann auch noch fast beliebig erweitert werden.
					Es stehen über die Erweiterungsmöglickeiten viele Schnittstellen zur Verfügung.
					Die Robby-Multifunktions-Platine 
					Die Motorplatine dient zu Steuerung der 
					beiden Motoren von Robby III, aber nicht nur 
					das. Die Motorplatine erfüllt gleich mehrere 
					Funktionen, um nicht allzu viele Zusatz-
					platinen zu haben und doch die wesentlichen 
					Funktionen des Mikrokontrollers, aber auch 
					von mechatronischen System zu demonstrieren. Dazu sind die entsprechenden 
					Schnittstellen und Anschlüsse auf der Platine 
					mit untergebracht. Die Platine wird auf den 
					Arduino Uno oder kompatible Version 
					aufgesteckt. 
					</p>
					
